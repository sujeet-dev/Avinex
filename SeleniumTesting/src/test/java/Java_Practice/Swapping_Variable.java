package Java_Practice;

public class Swapping_Variable {

	public static void main(String[] args) 
	{
	   int a=5;
	   int b=10;
	   
	   System.out.println("Before Swaping");
	   System.out.println(a);
	   System.out.println(b);
	   
	   int temp=a;
	   
	   a=b;
	   b=temp;
	   
	   System.out.println("Afetr Swapping");
	   System.out.println(a);
	   System.out.println(b);

	}

}
